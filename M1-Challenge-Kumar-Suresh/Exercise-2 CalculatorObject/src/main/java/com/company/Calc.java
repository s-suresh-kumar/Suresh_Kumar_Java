package com.company;

public class Calc{

    // integer methods

    // integer add
    public int add(int a1, int b1) {
        return a1+b1;
    }

    // integer subtract
    public int subtract(int a1, int b1) {
        return a1-b1;
    }

    // integer multiply
    public int multiply(int a1, int b1) {
        return a1*b1;
    }

    // integer divide
    public int divide (int a1, int b1) throws ArithmeticException {
        return a1/b1;
    }

    //double methods
    // double add
    public double add(double a1, double b1) {
        return a1+b1;
    }

    // double subtract
    public double subtract(double a1, double b1) {
        return a1-b1;
    }

    // double multiply
    public double multiply(double a1, double b1) {
        return a1*b1;
    }

    // double divide
    public double divide (double a1, double b1) throws ArithmeticException {
        return a1/b1;
    }

}